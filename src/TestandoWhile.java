
public class TestandoWhile {

	public static void main(String[] args) {
		int contador = 1;

		while (contador <= 5) {
			contador++;
			System.out.println(contador);
		}

		int i = 1;
		int multiplicador = 2;
		while (i <= 10) {
			System.out.println("2 * " + i + " = " + i * multiplicador);
			i++;
		}
	}

}
